def main() -> int:
    formula = load_json(FORMULA_JSON)
    pair_table = formula["pair_table"]
    target = observed_targets(pair_table, HIGH_DIGITS)
    best = best_rule_for_target(HIGH_DIGITS, set(target["blockers"]))
    best["target_blockers"] = target["blockers"]
    conditional = conditional_controls(HIGH_DIGITS, len(target["blockers"]), best)
    digit_scan = all_digit_set_scan(pair_table, best)
    stroke_path = stroke_path_analysis(target)
    result = {
        "schema": "high_block_blocker_origin_results.v1",
        "random_seed": RANDOM_SEED,
        "control_trials": CONTROL_TRIALS,
        "target": target,
        "observed_best": best,
        "conditional_controls": conditional,
        "digit_set_scan": {
            "same_size_count": digit_scan["same_size_count"],
            "same_size_exact_count": digit_scan["same_size_exact_count"],
            "same_size_exact_fraction": digit_scan["same_size_exact_fraction"],
            "same_size_p_exact": digit_scan["same_size_p_exact"],
            "all_set_count": digit_scan["all_set_count"],
            "all_exact_count": digit_scan["all_exact_count"],
            "all_exact_fraction": digit_scan["all_exact_fraction"],
            "all_p_exact": digit_scan["all_p_exact"],
            "top_rows": digit_scan["rows"][:20],
        },
        "stroke_path": stroke_path,
        "verdict": classify(best, conditional, digit_scan),
        "translation_delta": "NONE",
        "new_plaintext": False,
    }
    write_json(OUT_JSON, result)
    write_report(result)
    print(f"wrote {OUT_JSON.relative_to(ROOT)}")
    print(
        "best={rule} f1={f1:.3f} p_exact={p1:.5f} p_digit={p2:.5f} verdict={verdict}".format(
            rule=best["rule_id"],
            f1=best["f1"],
            p1=conditional["p_exact"],
            p2=digit_scan["same_size_p_exact"],
            verdict=result["verdict"],
        )
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
