#!/usr/bin/env python3
"""Algebraic digit-composition probe for the 469 pair table.

This pass asks whether the 55 unordered pair labels can be generated by:

    digit attributes -> simple pair operation -> bucket -> majority symbol

The bucket-to-symbol table is explicitly charged in MDL.  Large bucket
families such as natural min/max are kept in the search because they are useful
as lookup-disguise controls: they can reproduce the table, but only by making
one bucket per pair cell.

Mechanical only: no plaintext, no glossary, no semantic translation.
"""

