SIGMA = "*ABCEFILNORSTV"
SYMBOL_BITS = math.log2(len(SIGMA))
PAIR_COUNT = 55
RAW_LOOKUP_BITS = PAIR_COUNT * SYMBOL_BITS
INVENTORY_LOOKUP_NOTE = "multinomial primary-label inventory, reported only as secondary context"

